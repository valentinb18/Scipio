    private void txtClienteKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtClienteKeyPressed
        if (evt.getKeyCode() == java.awt.event.KeyEvent.VK_ENTER) {
            String clienteBusqueda = txtCliente.getText().toUpperCase().trim();
            boolean encontrado = false;
            
            if(clienteBusqueda.isEmpty() || clienteBusqueda.equals("CF") || clienteBusqueda.equals("CONSUMIDOR FINAL")){
                txtCliente.setText("CONSUMIDOR FINAL");
                txtBusquedaAceite.requestFocus();
                return;
            }
            
            for(Cliente c : Logica.Datos.listadoClientes){
                if(c.getNombre() != null && c.getNombre().toUpperCase().contains(clienteBusqueda)){
                    txtCliente.setText(c.getNombre().toUpperCase());
                    encontrado = true;
                    txtBusquedaAceite.requestFocus();
                    break;
                }
            }
            if(!encontrado){
                JOptionPane.showMessageDialog(this, "CLIENTE NO ENCONTRADO");
                txtCliente.selectAll();
            }
        }
    }//GEN-LAST:event_txtClienteKeyPressed


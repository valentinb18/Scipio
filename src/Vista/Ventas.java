    private void btnBusquedaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBusquedaActionPerformed
        String busquedaAceite = txtBusquedaAceite.getText().toUpperCase();
        boolean encontrado = false;
        
        for(Producto p : Logica.Datos.listadoLubricantes){
            if(p.getId().equals(busquedaAceite)){
                lblNombre.setText(p.getNombre());
                lblStockDisponible.setText(String.valueOf(p.getStock()));
                encontrado = true;
                break;
            }
        }
        if(!encontrado){
            JOptionPane.showMessageDialog(this, "PRODUCTO INEXSISTENTE EN INVENTARIO");
        }
    }//GEN-LAST:event_btnBusquedaActionPerformed

    private void btnRealizarVentaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRealizarVentaActionPerformed
        try{
            int cantidad = Integer.parseInt(txtCantidad.getText());
            
            String idEncontrado = txtBusquedaAceite.getText().toUpperCase();
            String nombreCliente = txtCliente.getText().trim().toUpperCase();
            
            boolean clienteCargado = false;
            if(nombreCliente.equals("CF") || nombreCliente.isEmpty()){
                nombreCliente = "CONSUMIDOR FINAL";
                clienteCargado = true;    
            }else{
                for(Cliente c : Logica.Datos.listadoClientes){
                    if(c.getNombre().toUpperCase().equals(nombreCliente)){
                        clienteCargado = true;
                        break;
                    }
                }
            }
            if(!clienteCargado){
                JOptionPane.showMessageDialog(this, "EL CLIENTE NO EXISTE. REGISTRELO O USE CONSUMIDOR FINAL");
                return;
            }
            
            for(Producto p : Logica.Datos.listadoLubricantes){
                if(p.getId().equals(idEncontrado)){
                    if(cantidad > p.getStock()){
                        JOptionPane.showMessageDialog(this, "NO HAY STOCK SUFICIENTE. CANTIDAD DISPONIBLE: " + p.getStock());
                        return;
                    }
                
                    int stockActual = p.getStock();
                    p.setStock(stockActual - cantidad);
                    
                    String fechaActual = new SimpleDateFormat("dd/MM/yyyy HH:mm").format(new Date());
                    Venta nuevaVenta = new Venta(fechaActual, nombreCliente, p.getNombre(), cantidad);
                    Logica.Datos.listadoVentas.add(nuevaVenta);
                    
                    JOptionPane.showMessageDialog(this, "VENTA REALIZADA");
                    limpiarCamposVenta();
                    return;
                }
            }
            JOptionPane.showMessageDialog(this, "PRODUCTO NO ENCONTRADO EN LA LISTA");
        }catch(NumberFormatException e){
            JOptionPane.showMessageDialog(this, "BUSQUE UN PRODUCTO Y LUEGO INGRESE UNA CANTIDAD VALIDA");
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, "ERROR AL REALIZAR LA VENTA: " + e.getMessage());
        }
    }//GEN-LAST:event_btnRealizarVentaActionPerformed

